2. Accumulator Options:
คำอธิบาย: อนุญาตให้ผลตอบแทนเพิ่มขึ้นแบบทบต้น (Compounding Growth) สูงสุด 5% ต่อ Tick ตราบใดที่ราคาของสินทรัพย์ยังคงอยู่ในช่วงที่กำหนด (Range).3 เหมาะสำหรับการทำกำไรจากตลาดที่มีความเสถียรหรือเคลื่อนไหวในกรอบ (Sideways Markets).24
การเทรด: ผู้ใช้เลือกอัตราการเติบโต (Growth Rate) ซึ่งจะกำหนดช่วงราคา (Barriers) ทั้งบนและล่างจากราคาปัจจุบัน.12 สามารถตั้งค่า Take Profit เพื่อล็อกกำไร หรือปิดการเทรดด้วยตนเองได้ก่อนกำหนด.12
เงื่อนไขชนะ:

ราคาสินทรัพย์ยังคงอยู่ในช่วงที่กำหนด (ภายใน Upper และ Lower Barriers).12
การจ่ายเงินจะทบต้นทุก Tick ที่ราคายังคงอยู่ในช่วงที่กำหนด.12


เงื่อนไขแพ้:

ราคาสินทรัพย์เคลื่อนไหวแตะหรือทะลุ Barrier ที่กำหนด (Knockout).12
ขาดทุนจำกัดที่เงินลงทุนเริ่มต้น (Initial Stake) ที่วางไว้ในสัญญา.12


3. Vanilla Options:
คำอธิบาย: เป็นสัญญาที่ให้สิทธิ์ (แต่ไม่ใช่ภาระผูกพัน) ในการซื้อ (Call Option) หรือขาย (Put Option) สินทรัพย์อ้างอิงในราคาที่กำหนดไว้ล่วงหน้า (Strike Price) ก่อนวันหมดอายุ (Expiration Date).22
การเทรด: เทรดเดอร์เลือก "Call" หากคาดว่าราคาจะสูงขึ้น หรือ "Put" หากคาดว่าราคาจะลดลง.23 ต้องกำหนด Strike Price และระยะเวลาสัญญา (Duration).23
เงื่อนไขชนะ:

Call Option: ราคาสุดท้าย (Final Price) ของสินทรัพย์สูงกว่า Strike Price ณ วันหมดอายุ.23
Put Option: ราคาสุดท้าย (Final Price) ของสินทรัพย์ต่ำกว่า Strike Price ณ วันหมดอายุ.23
มีศักยภาพกำไรไม่จำกัด (Unlimited Profit Potential) ยิ่งราคาเคลื่อนไหวในทิศทางที่คาดการณ์มากเท่าไหร่ กำไรก็ยิ่งสูงขึ้นเท่านั้น.22


เงื่อนไขแพ้:

ราคาไม่เคลื่อนไหวในทิศทางที่คาดการณ์ หรือไม่ถึง/เกิน Strike Price ณ วันหมดอายุ.25
ขาดทุนจำกัดที่เงินลงทุนเริ่มต้น (Premium หรือ Stake Amount) ที่จ่ายไปเพื่อซื้อสัญญา.22


4. Turbo Options:
คำอธิบาย: คล้ายกับ Vanilla Options แต่มีคุณสมบัติ "Knockout" เพิ่มเติม.22 ได้รับการจ่ายเงินหากการคาดการณ์ถูกต้องและราคา ไม่แตะหรือทะลุ Barrier ที่กำหนดไว้ล่วงหน้าตลอดระยะเวลาสัญญา.31
การเทรด: ผู้ใช้เลือก "Up" หากคาดว่าราคาจะสูงขึ้น หรือ "Down" หากคาดว่าราคาจะลดลง.36 ต้องกำหนด Barrier ที่ใกล้กับราคาปัจจุบันเพื่อศักยภาพกำไรที่สูงขึ้น แต่ก็มีความเสี่ยงสูงขึ้นด้วย.36 มีสัญญาแบบ Tick (ระยะสั้น 5-10 ticks) และ Minute (ระยะยาว 15 วินาทีถึงหลายวัน).36 สามารถตั้งค่า Take Profit เพื่อล็อกกำไร.36
เงื่อนไขชนะ:

ราคาเคลื่อนไหวในทิศทางที่คาดการณ์ (ขึ้นสำหรับ Up, ลงสำหรับ Down).36
และที่สำคัญคือ ราคาต้อง ไม่แตะหรือทะลุ Barrier ที่กำหนดไว้ตลอดระยะเวลาสัญญา.36
กำไรจะเท่ากับ Payout per point คูณด้วยระยะห่างระหว่างราคาสุดท้ายกับ Barrier.37


เงื่อนไขแพ้:

ราคาแตะหรือทะลุ Barrier ที่กำหนดไว้.36
สัญญาจะถูกยกเลิกก่อนกำหนด (Terminated Early) ทันที.37
ขาดทุนจำกัดที่เงินลงทุนเริ่มต้น (Initial Stake) ที่วางไว้ในสัญญา.36


5. Multipliers:
คำอธิบาย: ผสมผสานประโยชน์ของเลเวอเรจเข้ากับความเสี่ยงที่จำกัด ช่วยขยายศักยภาพกำไรได้สูงสุดถึง 2,000 เท่า หากตลาดเคลื่อนไหวในทิศทางที่คาดการณ์.15
การเทรด: เทรดเดอร์เลือกค่า Multiplier ที่ต้องการ (เช่น 30x ถึง 2000x) และทิศทางการเคลื่อนไหวของราคา (Up/Down).27 สามารถตั้งค่า Take Profit, Stop Loss และ Deal Cancellation เพื่อจัดการความเสี่ยงเพิ่มเติม.3
เงื่อนไขชนะ:

ราคาเคลื่อนไหวในทิศทางที่คาดการณ์.27
กำไรจะถูกคูณด้วยค่า Multiplier ที่เลือก ทำให้มีศักยภาพในการทำกำไรสูง.27


เงื่อนไขแพ้:

ราคาเคลื่อนไหวสวนทางกับที่คาดการณ์.27
ขาดทุนจะถูกจำกัดที่เงินลงทุนเริ่มต้น (Initial Stake) โดยมีฟังก์ชัน Stop-out อัตโนมัติที่ปิดตำแหน่งเมื่อราคาถึงระดับที่กำหนด.27
Deal Cancellation: คุณสมบัติเสริมที่ช่วยให้สามารถยกเลิกการเทรดได้ภายในกรอบเวลาที่เลือก (เช่น 5, 10, 15, 30, 60 นาที) หากราคาถึง Stop-out level ก่อนหมดเวลา Deal Cancellation เทรดจะถูกยกเลิกและเงินลงทุนเริ่มต้นจะถูกคืนโดยไม่ขาดทุน อย่างไรก็ตาม เมื่อเปิดใช้งาน Deal Cancellation คำสั่ง Stop-Loss และ Take-Profit จะไม่สามารถใช้งานได้.3


